a, b = 20, 14
#lebih dari
print(a, '>', b, '-', a > b)
#kurang dari
print(a, '<', b, '-', a < b)
#sama dengan
print(a, '==', b, '-', a == b)
#tidak sama dengan
print(a, '!=', b, '-', a != b)
#lebih dari atau sama dengan
print(a, '>=', b, '-', a >= b)
#kurang dari atau sama dengan
print(a, '<=', b, '-', a <= b)
