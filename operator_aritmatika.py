a, b = 20, 14
#penjumlahan
print(a, '+', b, '-', a + b)
#pengurangan
print(a, '-', b, '-', a - b)
#perkalian
print(a, '*', b, '-', a * b)
#pembagian
print(a, '/', b, '-', a / b)
#modulus
print(a, '%', b, '-', a % b)
#perpangkatam
print(a, '**', b, '-', a ** b)
#pembagian bulat
print(a, '//', b, '-', a // b)